public class Gaa {
    public static void main(String[] args) {
        //声明一个int类型 范围 正负21亿
        int age = 212222222;
        System.out.println(age);
        //声明一个byte类型-128 -127
        byte b1 = 127;
        System.out.println(b1);
        //声明一个short类型 -32768-32767
        short s1 = 32767;
        System.out.println(s1);
        //声明一个long类型 很大就对了 后面跟上l 小写l大写L都可以
        long l1 = 2222222222222l;
        System.out.println(l1);
    }
}
